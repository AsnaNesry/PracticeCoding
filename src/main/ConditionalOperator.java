package main;

public class ConditionalOperator {

	public static void main(String[] args) {
		int var1,var2;
		var1 = 20;
		var2 = (var1==1) ? 20 : 30;
		System.out.println("value of variable two is "+var2);
		var2 = (var1==20) ? 40 : 50;
		System.out.println("value of variable two is "+var2);
	}

}
